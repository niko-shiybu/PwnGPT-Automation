def _extract_json_block(raw: str) -> Dict[str, Any]:
    text = (raw or "").strip()
    try:
        return json.loads(text)
    except Exception:
        pass
    start = text.find("{")
    end = text.rfind("}")
    if start >= 0 and end > start:
        return json.loads(text[start : end + 1])
    raise ValueError("decider response does not contain valid JSON object")


def _fallback_decision(last_verify: Dict[str, Any]) -> DeciderDecision:
    summary = str(last_verify.get("summary", ""))
    if summary == "failed_syntax_error" or summary == "failed_syntax_or_logic_error":
        return DeciderDecision(
            failure="candidate exploit Python 语法错误，未进入验证或验证前已失败。",
            value_score=-80,
            next_action="优先修复 candidate_exploit.py 语法错误（缩进/转义/未闭合字符串等），通过语法门禁后再验证。",
            missing_measurements=[],
            notes=["fallback"],
        )
    if summary == "failed_all_measurements_empty":
        return DeciderDecision(
            failure="所有测量工具均失败，未获得任何实测数据。常见原因：工具和二进制不兼容（如 FmtStr offset 返回 0）、二进制交互方式特殊。",
            value_score=-70,
            next_action="强制切换测量方式：对于 fmt 题目，使用 fmt_scan_stack (AAAA%i$p 扫描) 或 fmt_scan_stack 手动探测；对于 stack 题目，用 disassemble(main) 分析反汇编。不要重复已失败的工具。",
            missing_measurements=[
                MeasurementRequest(1, "用替代工具重新测量关键偏移（不使用已失败的工具）", "已有工具全部失败，必须换方法", "high"),
            ],
            notes=["fallback"],
        )
    if summary == "failed_executor_measurement_error":
        return DeciderDecision(
            failure="测量工具返回错误（unresolved_facts 非空）。需要切换到不同的测量工具。",
            value_score=-65,
            next_action="不要重复同一个失败的测量工具。对于 fmt offset 测量失败：尝试 fmt_scan_stack 或手动 %p 扫描。对于 stack offset 失败：尝试 disassemble(main) 分析反汇编代码。",
            missing_measurements=[
                MeasurementRequest(1, "用不同于上一轮的测量工具重新测量", "上一轮工具失败需换方法", "high"),
            ],
            notes=["fallback"],
        )
    if summary == "failed_sigsegv":
        return DeciderDecision(
            failure="运行中触发 SIGSEGV，常见原因是 offset/目标地址/写入原语错误导致非法访问。",
            value_score=-60,
            next_action="先补关键测量（例如 ret_offset_bytes / fmt_arg_* / 目标地址确认），再按测量结果重写利用。",
            missing_measurements=[],
            notes=["fallback"],
        )
    if summary in {"failed_eof", "failed_no_shell_probe_marker", "failed_shell_not_alive"}:
        return DeciderDecision(
            failure="出现 EOF/未看到探测 marker/进程提前退出，疑似 I/O 同步或读写窗口不匹配。",
            value_score=-40,
            next_action="重新梳理交互流程（prompt tokens、菜单状态机），改用 sendlineafter/recvuntil 的更稳健同步；必要时增加分段读取窗口。",
            missing_measurements=[MeasurementRequest(1, "重新确认程序的交互提示和输入输出同步方式", "判断是否存在交互同步问题", "high")],
            notes=["fallback"],
        )
    if summary == "failed_hang_after_probe":
        return DeciderDecision(
            failure="探测后程序疑似卡死/无响应，可能是阻塞读取或进入了不可达状态。",
            value_score=-50,
            next_action="强制所有 recv 使用 timeout/限次轮询，避免 recvall；增加阶段 marker，确保每步都有可观测输出再进入 interactive。",
            missing_measurements=[],
            notes=["fallback"],
        )
    return DeciderDecision(
        failure="信息不足，无法定位根因：当前证据/实测事实不够支撑稳定利用。",
        value_score=-30,
        next_action="补齐关键事实（缺什么测什么），并基于新事实调整利用路线；避免在信息缺口下反复猜。",
        missing_measurements=[],
        notes=["fallback"],
    )


